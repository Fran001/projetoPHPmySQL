class Funcoes {
    private $id;
    private $nome;
    private $percentual_tempo;
    
    function getId() {
        return $this->id;
    }

    function getNome() {
        return $this->nome;
    }

    function getPercentual_tempo() {
        return $this->percentual_tempo;
    }

    function setId($id) {
        $this->id = $id;
    }

    function setNome($nome) {
        $this->nome = $nome;
    }

    function setPercentual_tempo($percentual_tempo) {
        $this->percentual_tempo = $percentual_tempo;
    }


}
